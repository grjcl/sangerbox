package com.pubmedplus.server.pojo;

public class MirnaTargetrna {
	private String geneID;
	private String miRNAID;
	private String miRNAName;
	private String database;
	private String ensgID;
	private String gType;

	public String getGeneID() {
		return geneID;
	}

	public void setGeneID(String geneID) {
		this.geneID = geneID;
	}

	public String getMiRNAID() {
		return miRNAID;
	}

	public void setMiRNAID(String miRNAID) {
		this.miRNAID = miRNAID;
	}

	public String getMiRNAName() {
		return miRNAName;
	}

	public void setMiRNAName(String miRNAName) {
		this.miRNAName = miRNAName;
	}

	public String getDatabase() {
		return database;
	}

	public void setDatabase(String database) {
		this.database = database;
	}

	public String getEnsgID() {
		return ensgID;
	}

	public void setEnsgID(String ensgID) {
		this.ensgID = ensgID;
	}

	public String getgType() {
		return gType;
	}

	public void setgType(String gType) {
		this.gType = gType;
	}

}
